-- Experiments: the atomic unit of research
-- Every research action that produces a result gets logged here

CREATE TABLE experiments (
    id TEXT PRIMARY KEY,  -- EXP-0001 format, auto-generated by CLI
    program TEXT NOT NULL REFERENCES programs(id),
    status TEXT NOT NULL DEFAULT 'open'
        CHECK (status IN ('open', 'running', 'complete', 'failed', 'superseded')),
    source TEXT NOT NULL,  -- human/mlee, codex/task-abc, slack-bot/channel-research

    -- The science
    hypothesis TEXT,
    parameters JSONB NOT NULL DEFAULT '{}',
    results JSONB,
    finding TEXT,

    -- Provenance
    git_commit TEXT,      -- full SHA
    git_repo TEXT,        -- remote URL or local path
    git_branch TEXT,
    data_sources TEXT[],  -- stac:// references
    tags TEXT[] NOT NULL DEFAULT '{}',

    -- Relationships
    direction_id TEXT,    -- FK added after directions table exists
    related TEXT[] NOT NULL DEFAULT '{}',  -- experiment IDs

    -- Timing
    run_at TIMESTAMPTZ,       -- when the experiment was actually executed
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Indexes for the query patterns from the PRD
CREATE INDEX idx_experiments_program ON experiments (program);
CREATE INDEX idx_experiments_status ON experiments (status);
CREATE INDEX idx_experiments_source ON experiments (source);
CREATE INDEX idx_experiments_direction ON experiments (direction_id);
CREATE INDEX idx_experiments_created ON experiments (created_at DESC);
CREATE INDEX idx_experiments_params ON experiments USING GIN (parameters);
CREATE INDEX idx_experiments_tags ON experiments USING GIN (tags);
CREATE INDEX idx_experiments_fts ON experiments USING GIN (
    to_tsvector('english', coalesce(hypothesis, '') || ' ' || coalesce(finding, ''))
);

-- Auto-update updated_at
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER experiments_updated_at
    BEFORE UPDATE ON experiments
    FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- Sequence for experiment IDs
CREATE SEQUENCE experiment_id_seq START 1;
